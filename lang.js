document.addEventListener("DOMContentLoaded", () => {
    // 1. Determine Language (Default TR if not set)
    let currentLang = localStorage.getItem('site_lang') || 'tr';

    // 2. Initial Application
    applyLanguage(currentLang);
    updateToggleButton(currentLang);

    // 3. Attach Event Listener to the Toggle Button
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.preventDefault();
            // Toggle logic
            currentLang = currentLang === 'tr' ? 'en' : 'tr';
            localStorage.setItem('site_lang', currentLang);

            applyLanguage(currentLang);
            updateToggleButton(currentLang);
        });
    }
});

function applyLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        // Only update if key exists in our dictionary
        if (dictionary[lang] && dictionary[lang][key]) {
            // Some elements might contain HTML (like hero_title), use innerHTML
            el.innerHTML = dictionary[lang][key];
        }
    });

    // Update document lang attribute for SEO/Accessibility
    document.documentElement.lang = lang;
}

function updateToggleButton(lang) {
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        // If current is TR, button should say EN (to switch to English)
        if (lang === 'tr') {
            langBtn.innerHTML = '<i class="fa-solid fa-globe"></i> EN';
            // You can also adjust font-weight or active state here if you have both TR | EN texts visible
        } else {
            langBtn.innerHTML = '<i class="fa-solid fa-globe"></i> TR';
        }
    }
}
